<script>
    $('.hide_status').hide();
    var cnt= '{{$cnt}}'
    // if(cnt>10){
        var table = $('#product_system_reports').DataTable();
    // }

    $('.table-filter-status').on('click', function() {
        var selectedValue = $(this).attr("data-value");
        table.columns(6).search(selectedValue).draw();
    });
    $('#myCustomSearchBox').keyup(function() {
        table.search($(this).val()).draw(); // this  is for customized searchbox with datatable search feature.
    })

    $('.deletebulk').hide();
    $("#example-select-all").click(function() {
        var len = $("[name='select_all[]']:checked").length;
        if (len > 1) {
            $(".deletebulk").show();
        } else {
            $(".deletebulk").hide();
        }
        $('.checkSingle').not(this).prop('checked', this.checked);
        $('.checkSingle').click(function() {
            if ($('.checkSingle:checked').length == $('.checkSingle').length) {
                $('#example-select-all').prop('checked', true);
            } else {
                $('#example-select-all').prop('checked', false);
            }
        });
    });
    $('.checkSingle').click(function() {
        var len = $("[name='select_all[]']:checked").length;
        if (len > 1) {
            $(".deletebulk").show();
        } else {
            $(".deletebulk").hide();
        }
    });
    function saveData() {
        report_name = document.getElementById('report_name').value;
        if (report_name == "") {
            $("#rp").text("Enter Report Name");
            $("#rp").addClass("text-danger");
            return false;
        }
        product_system_id = document.getElementById('product_system_id').value;
        if (product_system_id == 0) {
            $("#ep").text("Select Product System");
            $("#ep").addClass("text-danger");
            return false;
        }
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="_token"]').attr('content')
            }
        });
        $.ajax({
            url: "{{ url('/reports/product_system') }}",
            method: 'POST',
            data: {
                report_name: report_name,
                product_system_id: product_system_id,
            },
            success: function(data) {
                const Toast = Swal.mixin({
                    toast: true,
                    position: "bottom-end",
                    showConfirmButton: false,
                    timer: 5000,
                });
                if (data.message == 'Permission Denied!') {
                    Toast.fire({
                        icon: 'warning',
                        title: "Permission Denied!",
                    })
                } else {
                    Toast.fire({
                        icon: 'success',
                        title: "Report Created",
                    })
                }
                document.getElementById('product_system_id').value = 0;
                $('.bd-example-modal-sm').hide();
                $('body').removeClass('modal-open');
                $('.modal-backdrop').remove();
                let url = "{{ url('reports/product_system') }}";
                document.location.href = url;
            }
        })
    }
</script>
