<script>
    var cnt = '{{$cnt}}'
    $('.hide_status').hide();
    // if (cnt > 10) {
    var table = $('#process_simulation_reports').DataTable({
        "iDisplayLength": 100,
        "dom": "lrtip"
    });
    $('.table-filter-status').on('click', function() {
        var selectedValue = $(this).attr("data-value");
        table.columns(7).search(selectedValue).draw();
    });
    $('#myCustomSearchBox').keyup(function() {
        table.search($(this).val()).draw(); // this  is for customized searchbox with datatable search feature.
    })
    //}
    $(".deletebulk").hide();

    $("#example-select-all").click(function() {
        var len = $("[name='select_all[]']:checked").length;
        if (len > 1) {
            $(".deletebulk").show();
        } else {
            $(".deletebulk").hide();
        }
        $('.checkSingle').not(this).prop('checked', this.checked);
        $('.checkSingle').click(function() {
            if ($('.checkSingle:checked').length == $('.checkSingle').length) {
                $('#example-select-all').prop('checked', true);
            } else {
                $('#example-select-all').prop('checked', false);
            }
        });
    });
    $('.checkSingle').click(function() {
        var len = $("[name='select_all[]']:checked").length;
        if (len > 1) {
            $(".deletebulk").show();
        } else {
            $(".deletebulk").hide();
        }
    });

    async function product_simulation_typelist(selectedvalue){
        $("#valid-error").hide();
        try {
            result = await $.ajax({
                url: "{{url('/product_system/product_simulation_typelist')}}",
                method: 'get',
                data :{
                    "parameters" : selectedvalue,
                    "report" : 1
                }  
            });
            $("#simulation_type").replaceWith(result.html);
            if(result.status ==false){
                $("#valid-error").show();
            }
        } catch (error) {
            console.error(error);
        }
    }

    async function saveData() {
        let result;
        $("#name-error").hide();
        report_name = document.getElementById('report_name').value;
        if (report_name == "") {
            $("#name-error").show();
            return false;
        }
        process_id = document.getElementById('process_simulation').value;
        if (process_id == "") {
            $("#process-error").show();
            return false;
        }
        dataset_id = document.getElementById('dataset').value;
        if (dataset_id == "") {
            $("#dataset-error").show();
            return false;
        }
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="_token"]').attr('content')
            }
        });
        try {
            result = await $.ajax({
                url: "{{ url('/reports/process_analysis') }}",
                method: 'POST',
                data: {
                    report_name: report_name,
                    dataset_id: dataset_id,
                    process_id: process_id,
                }
            });
            if (result && result.success == "pending") {
                swal.fire({
                    text: "Report is in Process!, Once Process Done You can proceed for next report",
                    confirmButtonText: 'Close',
                    confirmButtonClass: 'btn btn-sm btn-danger',
                    width: '350px',
                    height: '10px',
                    icon: 'warning',
                });
                document.getElementById('report_name').value = "";
                $(".generate_report").modal("hide");
                // let url = "{{ url('reports/experiment') }}";
                // window.open(url, '_blank');
                return false;
            }
            const Toast = Swal.mixin({
                toast: true,
                position: "bottom-end",
                showConfirmButton: false,
                timer: 5000,
            });
            Toast.fire({
                icon: 'success',
                title: "Report Created",
            })
            location.reload();
        } catch (error) {
            console.error(error);
        }
    }

</script>
